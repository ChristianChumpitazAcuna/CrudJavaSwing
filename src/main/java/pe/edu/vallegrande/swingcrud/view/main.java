    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        int fila = tableData.getSelectedRow();
        if (fila == -1) {
            showModal("No se ha Seleccionado ningun item");
        } else {
            btnCancel.setVisible(true);
            btnAdd.setEnabled(false);
            btnEdit.setEnabled(true);
            btnDelete.setEnabled(true);

            id = Integer.parseInt((String) tableData.getValueAt(fila, 0).toString());
            String name = (String) tableData.getValueAt(fila, 1);
            String lastName = (String) tableData.getValueAt(fila, 2);
            String sex = (String) tableData.getValueAt(fila, 3);
            boolean overAge = (boolean) tableData.getValueAt(fila, 4);

            txtId.setText("" + id);
            txtName.setText(name);
            txtLastName.setText(lastName);
            txtSex.setSelectedItem(sex);
            txtOverAge.setSelected(overAge);

        }
    }//GEN-LAST:event_tableDataMouseClicked

