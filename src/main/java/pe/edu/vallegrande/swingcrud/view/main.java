    private void listData() {
        try {
            String sql = "SELECT * FROM personas";
            System.out.println("QUERY: " + sql);

            cn = con.getConexion();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            modelo = (DefaultTableModel) tableData.getModel();
            modelo.setRowCount(0);

            while (rs.next()) {
                Object[] person = new Object[5];
                person[0] = rs.getInt("id");
                person[1] = rs.getString("nombres");
                person[2] = rs.getString("apellidos");
                person[3] = rs.getString("sexo");
                person[4] = rs.getBoolean("adulto");

                modelo.addRow(person);
            }

            tableData.setModel(modelo);

        } catch (SQLException e) {
            System.out.println("Error al listar: " + e);
            showModal("Error al listar: " + e.getMessage());
        }
    }

    private void saveUser() {
        String name = txtName.getText();
        String lastName = txtLastName.getText();
        boolean overAge = txtOverAge.isSelected();

        int sex = txtSex.getSelectedIndex();
        String sexValue = txtSex.getSelectedItem().toString();

        if (name.equals("") || lastName.equals("")
                || (sex == 0 || sex > 3)) {
            showModal("Datos Invalidos");
        } else {
            try {
                String sql = "INSERT INTO personas (nombres, apellidos, adulto, sexo) "
                        + "VALUES('" + name + "', '" + lastName + "', " + (overAge ? 1 : 0) + ", '" + sexValue + "')";
                System.out.println("QUERY: " + sql);
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);

                showModal("Registro Exitoso");

            } catch (SQLException e) {
                System.out.println("Error al insertar: " + e);
                showModal("Error al insertar: " + e.getMessage());
            }
        }
    }

    private void updateUser() {
        String name = txtName.getText();
        String lastName = txtLastName.getText();
        boolean overAge = txtOverAge.isSelected();

        int sex = txtSex.getSelectedIndex();
        String sexValue = txtSex.getSelectedItem().toString();

        if (name.equals("") || lastName.equals("")
                || (sex == 0 || sex > 3)) {
            showModal("Se requieren todos los campos");
        } else {
            try {
                String sql = "UPDATE personas SET nombres = '" + name + "',"
                        + "apellidos = '" + lastName + "', sexo= '" + sexValue + "', adulto= '" + (overAge ? 1 : 0)
                        + "' WHERE id=" + id;
                System.out.println("QUERY: " + sql);

                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);

                showModal("Actualización Exitosa");

            } catch (SQLException e) {
                System.out.println("Error al actualizar: " + e);
                showModal("Error al actualizar: " + e.getMessage());
            }
        }
    }

    private void deleteUser() {
        int fila = tableData.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        } else {
            try {
                String sql = "DELETE FROM personas WHERE id=" + id;
                cn = con.getConexion();
                st = cn.createStatement();
                st.executeUpdate(sql);

                showModal("Eliminación Exitosa");

            } catch (SQLException e) {
                System.out.println("Error al eliminar: " + e);
                showModal("Error al eliminar: " + e.getMessage());
            }
        }
    }

